// require mongoose module
const mongoose = require('mongoose');
const Schema = mongoose.Schema;

// define schema
const UserSchema = new Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

// model
mongoose.model('users', UserSchema);